// Compound Assignment Operator!
class Main {
  public static void main(String[] args) {
   int D = 7;
   System.out.println("Addition: " + (D+=1));
   System.out.println("Subtraction: " + (D-=1));
   System.out.println("Muliplication: " + (D*=2));
   System.out.println("Division: " + (D/=2));
  }
} 
