class Main {
  public static void main(String[] args) {
    final double Pie=3.14; 
    final double Circle1=5.56; 
    final double Circle2=42.121;
    int Circle3=5; 
    /* You can calculate multiple integers in a single line as well although when combining it with "", other () are required to separate them. 
    */  
    System.out.println("(5.56 cm) Radius of Circle1: " + (Pie * Circle1 * Circle1 + " Cm"));
    System.out.println("(42.121 cm) Radius of Circle2: " + (Pie * Circle2 * Circle2 + " Cm"));
    System.out.println("(5 cm) Radius of Circle3: " + (Pie * Circle3 * Circle3 + " Cm")); 
    
  }
}
